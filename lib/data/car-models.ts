/**
 * Get car models by brand
 */
export function getCarsByBrand(brand: string): CarModel[] {
    return CAR_MODELS.filter(car => car.brand === brand);
}

/**
 * Get car models by multiple brands
 */
export function getCarsByBrands(brands: string[]): CarModel[] {
    return CAR_MODELS.filter(car => brands.includes(car.brand));
}

/**
 * Get a single car by ID
 */
export function getCarById(id: string): CarModel | undefined {
    return CAR_MODELS.find(car => car.id === id);
}

/**
 * Get all unique brands
 */
export function getAllBrands(): string[] {
    return Array.from(new Set(CAR_MODELS.map(car => car.brand)));
}
