// Helper function to get all unique car makes
export function getAllMakes(): string[] {
  const makes = new Set(allCars.map(car => car.make));
  return Array.from(makes).sort();
}

// Helper function to filter cars by make
export function getCarsByMake(make: string): Car[] {
  return allCars.filter(car =>
    car.make.toLowerCase() === make.toLowerCase()
  );
}

// Helper function to get cars by body type
export function getCarsByBodyType(bodyType: string): Car[] {
  return allCars.filter(car =>
    car.bodyType.toLowerCase() === bodyType.toLowerCase()
  );
}

// Helper function to get all unique body types
export function getAllBodyTypes(): string[] {
  const types = new Set(allCars.map(car => car.bodyType));
  return Array.from(types).sort();
}

// Helper function to get cars within a price range
export function getCarsByPriceRange(minPrice: number, maxPrice: number): Car[] {
  return allCars.filter(car =>
    car.pricing.exShowroom.min >= minPrice &&
    car.pricing.exShowroom.max <= maxPrice
  );
}
