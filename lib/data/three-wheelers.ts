function get3WImageUrl(brand: string, model: string): string | null {
    const folder = BRAND_IMG_FOLDER[brand]
    if (!folder) return null
    const urls = vehicleImageUrls as Record<string, string>
    // Strip fuel/body suffix e.g. "Maxima C/CNG" → "Maxima C", "King Kargo CNG HD/PF" → "King Kargo CNG HD"
    const baseName = model.split('/')[0].trim()
    const toSlug = (s: string) => s.toLowerCase().replace(/[^a-z0-9\s-]/g, '').replace(/\s+/g, '-').replace(/-+/g, '-')
    const slug = toSlug(baseName)

    // 1. Exact match
    if (urls[`3w/${folder}/${slug}.jpg`]) return urls[`3w/${folder}/${slug}.jpg`]

    // 2. Progressive prefix: "gem-paxx-3-seater" → try "gem-paxx-3" → "gem-paxx" → "gem"
    const parts = slug.split('-')
    for (let i = parts.length - 1; i >= 2; i--) {
        const shorter = parts.slice(0, i).join('-')
        if (urls[`3w/${folder}/${shorter}.jpg`]) return urls[`3w/${folder}/${shorter}.jpg`]
    }

    // 3. Substring match: find any stored key whose slug is contained in ours or vice versa
    const prefix = `3w/${folder}/`
    for (const key of Object.keys(urls)) {
        if (!key.startsWith(prefix)) continue
        const stored = key.slice(prefix.length, -4)
        if (slug.startsWith(stored) || stored.startsWith(slug)) return urls[key]
    }

    return null
}

function lookup3WColors(brand: string, model: string) {
    return THREE_WHEELER_MODEL_COLORS[`${brand} ${model}`] ?? []
}

const NOW = new Date().toISOString()
const CURRENT_YEAR = new Date().getFullYear()

function subCatToType3W(subCat: string): ThreeWheelerType {
    if (subCat === 'electric') return 'electric'
    if (subCat === 'cargo' || subCat === 'cargo_cng') return 'cargo'
    if (subCat === 'school_van') return 'school_van'
    return 'passenger'
}

function subCatToFuel3W(subCat: string): ThreeWheelerFuelType {
    if (subCat === 'electric') return 'electric'
    if (subCat === 'cargo_cng') return 'cng'
    if (subCat === 'cargo')    return 'diesel'
    return 'cng'
}

function buildThreeWheelerEntry(
    model: string,
    type: ThreeWheelerType,
    fuelType: ThreeWheelerFuelType,
    brand: string,
    brandId: string,
    idx: number,
    dealerId: string
): ThreeWheelerVehicle {
    const slug = modelToSlug(model)
    const imageUrl = `/data/brand-model-images/3w/${brandId}/${slug}.jpg`
    const enrichment = get3WModelEnrichment(brandId, model)
    return {
        id:                     `catalog-3w-${brandId}-${idx}`,
        dealer_id:              dealerId,
        type,
        brand,
        model,
        variant:                null,
        year:                   CURRENT_YEAR,
        fuel_type:              fuelType,
        engine_cc:              enrichment?.engine_cc ?? null,
        max_power:              enrichment?.max_power ?? null,
        torque:                 enrichment?.torque ?? null,
        battery_kwh:            null,
        range_km:               enrichment?.range_km ?? null,
        charging_time_hours:    null,
        battery_warranty_years: null,
        transmission:           enrichment?.transmission ?? 'Automatic',
        payload_kg:             type === 'cargo' ? (enrichment?.gvw_kg ?? 500) : null,
        body_type:              null,
        passenger_capacity:     type === 'passenger' || type === 'electric' ? 3 : type === 'school_van' ? 8 : null,
        max_speed_kmph:         null,
        mileage_kmpl:           enrichment?.mileage_kmpl ?? null,
        cng_mileage_km_per_kg:  fuelType === 'cng' ? 28 : null,
        permit_type:            null,
        gvw_kg:                 enrichment?.gvw_kg ?? null,
        fame_subsidy_eligible:  false,
        bs6_compliant:          true,
        ex_showroom_price_paise: enrichment?.ex_showroom_price_paise ?? 0,
        on_road_price_paise:    null,
        emi_starting_paise:     null,
        stock_status:           'available',
        colors:                 lookup3WColors(brand, model),
        images:                 [imageUrl],
        brochure_url:           null,
        description:            null,
        features:               [],
        video_url:              null,
        is_featured:            false,
        status:                 'active',
        views:                  0,
        created_at:             NOW,
        updated_at:             NOW,
    }
}
