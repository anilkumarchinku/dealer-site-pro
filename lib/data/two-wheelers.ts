/**
 * lib/data/two-wheelers.ts
 * Static 2-Wheeler catalog — used as a fallback when a dealer has no DB inventory.
 * Models and prices are approximate India ex-showroom (2024–25).
 */

import type { TwoWheelerVehicle, TwoWheelerType, TwoWheelerFuelType } from '@/lib/types/two-wheeler'
import brandData from '@/lib/data/brand-models.json'
import { brandNameToId, modelToSlug } from '@/lib/utils/brand-model-images'
import { getModelEnrichment } from '@/lib/data/2w-brand-data'

const NOW = new Date().toISOString()
const CURRENT_YEAR = new Date().getFullYear()

// ── Brands in the "electric" group in brand-models.json ──────────────────────
const ELECTRIC_GROUP_BRANDS = new Set(
    (brandData.twoWheelers.electric as { brand: string }[]).map(b => b.brand)
)

function subCatToType(subCat: string, isElectricBrand: boolean): TwoWheelerType {
    if (isElectricBrand || subCat === 'electric') return 'electric'
    if (subCat === 'scooters') return 'scooter'
    return 'bike'
}

function buildTwoWheelerEntry(
    model: string,
    type: TwoWheelerType,
    brand: string,
    brandId: string,
    idx: number,
    dealerId: string
): TwoWheelerVehicle {
    const fuelType: TwoWheelerFuelType = type === 'electric' ? 'electric' : 'petrol'
    const slug = modelToSlug(model)
    const imageUrl = `/data/brand-model-images/2w/${brandId}/${slug}.jpg`

    // Try to enrich from public/data/2w/*.json files
    const enrichment = getModelEnrichment(brandId, model)

    return {
        id:                      `catalog-2w-${brandId}-${idx}`,
        dealer_id:               dealerId,
        type,
        brand,
        model,
        variant:                 enrichment?.variant ?? null,
        year:                    CURRENT_YEAR,
        fuel_type:               fuelType,
        engine_cc:               enrichment?.engine_cc ?? null,
        battery_kwh:             enrichment?.battery_kwh ?? null,
        range_km:                enrichment?.range_km ?? null,
        charging_time_hours:     null,
        battery_warranty_years:  null,
        top_speed_kmph:          enrichment?.top_speed_kmph ?? null,
        mileage_kmpl:            enrichment?.mileage_kmpl ?? null,
        ex_showroom_price_paise: enrichment?.ex_showroom_price_paise ?? 0,
        on_road_price_paise:     null,
        emi_starting_paise:      null,
        stock_status:            enrichment?.stock_status ?? 'available',
        colors:                  enrichment?.colors ?? [],
        images:                  [imageUrl],
        brochure_url:            null,
        bs6_compliant:           true,
        fame_subsidy_eligible:   fuelType === 'electric',
        all_variants:            enrichment?.all_variants ?? [],
        wheelbase_mm:            enrichment?.wheelbase_mm ?? null,
        length_mm:               enrichment?.length_mm ?? null,
        width_mm:                enrichment?.width_mm ?? null,
        height_mm:               enrichment?.height_mm ?? null,
        description:             enrichment?.description ?? null,
        features:                enrichment?.features ?? [],
        video_url:               null,
        is_featured:             false,
        status:                  'active',
        views:                   0,
        created_at:              NOW,
        updated_at:              NOW,
    }
}

